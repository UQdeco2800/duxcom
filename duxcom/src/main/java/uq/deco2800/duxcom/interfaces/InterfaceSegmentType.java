package uq.deco2800.duxcom.interfaces;

/**
 * The game interfaces by type
 * Created by liamdm on 16/08/2016.
 */
public enum InterfaceSegmentType {
    /**
     * The test interface
     */
    TEST,
    /**
     * The load screen pre-game
     */
    LOAD_SCREEN,
    /**
     * Used to show settings
     */
    SETTINGS,
    /**
     * Used to play game itself
     */
    GAME,
    /**
     * The map creator
     */
    MAP_CREATOR,
    /**
     * For objective testing
     */
    OBJECTIVE_TESTING,
    /**
     * The navigator to access different game maps
     */
    OVERWORLD,
    /**
     * The pre-game hero select
     */
    HERO_SELECT,
    /**
     * Entry screen for login
     */
    LOGIN_SCREEN,
    /**
     * Allows user to register
     */
    USER_REGISTRATION,
    /**
     * The multiplayer lobby
     */
    LOBBY,
    /**
     * The player rewards screen.
     */
    PLAYER_REWARDS,
    /**
     *  The leader board screen
     */
    LEADER_BOARD
}
